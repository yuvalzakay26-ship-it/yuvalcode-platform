// Build-time distribution generator.
//
// Runs at `closeBundle` time via the Vite plugin in vite.config.js. Emits:
//
//   dist/sitemap.xml
//   dist/feeds/<slug>.xml        (RSS 2.0)
//   dist/feeds/<slug>.atom.xml   (Atom 1.0)
//   dist/feeds/<slug>.json       (JSON Feed 1.1)
//   dist/feeds/index.txt         (human-readable feed catalog)
//
// The generator imports the in-source distribution modules — they are pure
// JS and therefore equally safe at build time as at runtime. Vite resolves
// the import graph with its existing JSX pipeline (we run this script inside
// the Vite plugin context, where `import.meta.env` is available, so the
// `defineCollection` warnings fire and the entire content layer hydrates
// the same way it does at runtime).

import { mkdir, writeFile } from "node:fs/promises";
import { dirname, join } from "node:path";

export async function generateDistribution({ outDir, logger }) {
    const distDir = outDir;
    const log = (msg) => logger?.info?.(`[distribution] ${msg}`) || console.log(`[distribution] ${msg}`);

    // Dynamic import — must happen *after* Vite has hydrated the source.
    // The build context guarantees the source is reachable; we resolve via
    // file:// URLs so node ESM does not need Vite middleware here.
    const distribution = await import("../../src/lib/distribution/index.js");
    const { COLLECTIONS, flatEntries } = await import("../../src/content/collections.js");
    const { getAllEntries } = await import("../../src/lib/content/index.js");

    const allEntries = getAllEntries();
    log(`Resolved ${allEntries.length} editorial entries across ${Object.keys(COLLECTIONS).length} collections.`);

    // 1. Sitemap.
    const sitemapXml = distribution.buildSitemap({ entries: allEntries });
    await writeOutput(join(distDir, "sitemap.xml"), sitemapXml);
    log(`Wrote sitemap.xml (${sitemapXml.length} bytes).`);

    // 2. Feeds — global + per editorial collection.
    const feedSlugs = distribution.getFeedSlugs();
    const feedsDir = join(distDir, "feeds");

    for (const slug of feedSlugs) {
        let entries = [];
        if (slug === "all") {
            entries = allEntries;
        } else {
            const config = distribution.getCollectionForFeedSlug(slug);
            if (!config) continue;
            entries = allEntries.filter((e) => e.type === config.type);
        }

        // Sort recency-first for feed readers — undated entries sink.
        entries = entries.slice().sort(byDateDesc);

        const model = distribution.buildFeedModel({ slug, entries });

        const rss = distribution.renderRss(model);
        const atom = distribution.renderAtom(model);
        const json = distribution.renderJsonFeed(model);

        await writeOutput(join(feedsDir, `${slug}.xml`), rss);
        await writeOutput(join(feedsDir, `${slug}.atom.xml`), atom);
        await writeOutput(join(feedsDir, `${slug}.json`), json);
    }
    log(`Wrote ${feedSlugs.length * 3} feed files (RSS + Atom + JSON Feed × ${feedSlugs.length}).`);

    // 3. Feed catalog — human-readable index of every feed.
    const catalog = distribution.buildFeedIndex({ feedSlugs });
    await writeOutput(join(feedsDir, "index.txt"), catalog);

    // 4. Mirror feed availability into the static catalog log so the build
    //    output makes it obvious what landed.
    log(`Distribution snapshot — surfaces=${distribution.getStaticSurfaces().length}, feeds=${feedSlugs.length}, entries=${allEntries.length}.`);

    // 5. Touch the placeholder readme.
    const readme = [
        `# Distribution outputs`,
        ``,
        `Auto-generated by scripts/distribution/build-distribution.mjs at every build.`,
        `Do not edit by hand.`,
        ``,
        `## Feeds`,
        ...feedSlugs.flatMap((slug) => [
            ``,
            `### ${slug}`,
            `- /feeds/${slug}.xml`,
            `- /feeds/${slug}.atom.xml`,
            `- /feeds/${slug}.json`,
        ]),
        ``,
    ].join("\n");
    await writeOutput(join(feedsDir, "README.md"), readme);

    return {
        sitemapBytes: sitemapXml.length,
        feedSlugs,
        entriesProcessed: allEntries.length,
    };
}

async function writeOutput(path, content) {
    await mkdir(dirname(path), { recursive: true });
    await writeFile(path, content, "utf8");
}

function byDateDesc(a, b) {
    if (!a.date && !b.date) return 0;
    if (!a.date) return 1;
    if (!b.date) return -1;
    return b.date.localeCompare(a.date);
}
